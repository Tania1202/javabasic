package class4;

public class ifElseConditions {
    public static void main(String[] args) {
        boolean rain = true;
        if (rain) {
            System.out.println("Lets take an umbrella");
        } else {
            System.out.println("Lets go for a walk");
        }
        boolean brake=true;
        if(brake){
            System.out.println("Lets take a brake");
        }else{
            System.out.println("Lets continue the class ");
        }
    }
}
