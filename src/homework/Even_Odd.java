public class Even_Odd {
    public static void main(String[] args) {
        //Write a Java Program to check whether number is Even or Odd.
        int number = -1000;
        if (number % 2 == 0) {
            System.out.println("Number is Even");
        } else {
            System.out.println("Number is Odd");
        }
    }
}
