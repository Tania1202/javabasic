package class3;

public class VariablesDemo2 {
    public static void main(String[] args) {
        int number=10;
        System.out.println(number);
        number=20;
        System.out.println(number);

        int age=10;
        number=age;
        System.out.println(number);
        age=number+10;
        System.out.println(age);
        age=age+20;
        System.out.println(age);
    }
}
